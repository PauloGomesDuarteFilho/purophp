		Muitos dentistas recomendam a extração de dentes inclusos que nasceram apenas parcialmente. As bactérias podem se instalar em volta de um dente que nasceu parcialmente, causando uma infecção, a qual pode se estender para o osso adjacente e tornar-se um problema ainda mais sério. Os dentes inclusos continuam tentando atravessar o tecido da gengiva mesmo quando não há espaço suficiente para acomodá-los. A constante pressão causada por esta tentativa de erupção pode acabar afetando as raízes dos dentes vizinhos. Remover um dente incluso pode evitar uma infecção, danos aos dentes e osso adjacentes, além de evitar um sofrimento futuro.</p>

