		<p class="Cycle fs-20">Siso significa juízo, e é o nome dado aos dentes terceiros molares permanentes devido ao fato deles nascerem por volta dos 17/ 21 anos, ou seja, próximo de quando se adquire a chamada maioridade ou idade da razão ou do juízo. Há muitos anos, o homem necessitava caçar para ter o que comer. Não havia fogo para cozinhar os alimentos para deixá-los com uma consistência mais mole. Assim, ele precisava fazer grandes esforços mastigatórios e, com isso, possuía uma arcada dentária maior e mais desenvolvida. Com a descoberta do fogo, o desenvolvimento da raça humana e a ingestão de alimentos processados, a arcada foi diminuindo de tamanho. Por ter ocorrido essa redução da arcada com o passar do tempo, o que se vê hoje é que muitas vezes não há espaço para que o dente do siso nasça e, com isso, ele pode ficar retido ou semi-retido ao osso.</p>

